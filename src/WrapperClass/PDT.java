package WrapperClass;

public class PDT {
    public static void main(String[] args) {
        //unboxing

        Integer num1 = 4;
        int a = num1;
        System.out.println(a);

        //boxing
        float numFloat = 10.4f;
        int num = (int) numFloat;
        System.out.println(num);
    }
}
